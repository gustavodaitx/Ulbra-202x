a=float(input("Digite um número real:  "))
b=float(input("Digite outro número real:  "))
c=float(input("Digite mais um número real:  "))

prod=a*b*c
soma=a+b+c
subt=a-b-c
result=prod+soma+subt

print(f'O produto desses números é:{prod}')
print(f'A soma desses números é:{soma}')
print(f'A subtração desses números é:{subt}')

print(f'A soma dos resultados das operações acima é:{result}')


